// Set images from SVG traces so I don't have to embed them directly in the HTML
function setImage(img, d) {
  setTimeout(() => {
    const element = document.getElementById(img);
    if (element !== null) {
      element.setAttribute('d', d);
    }
    else {
      for (i in d) {
        const element = document.getElementById(img + i);
        if (element !== null) {
          element.setAttribute('d', d[i]);
        }
      }
    }
  });
}

